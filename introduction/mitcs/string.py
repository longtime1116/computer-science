print("0123456789"[4:6]) #=> 45
print("0123456789"[:6]) #=> 012345
print("0123456789"[4:]) #=> 456789
print("0123456789"[:]) # copy(0123456789)

print("0123456789"[1:9:3]) #=> 147
print("0123456789"[1:9:-3]) #=> 何も取り出せない
print("0123456789"[9:1:-3]) #=> 963
print("0123456789"[::3]) #=> 0369
print("0123456789"[::-1]) #=> 9876543210
